const fs = require('fs')

function toCHexArray(arr) {
    let c = 0
    var hex_str = Array.from(arr, function (byte) {
        return '0x' + ('0' + (byte & 0xFF).toString(16)).slice(-2) + ((++c % 16) == 0 ? ',\n' : ',');
    }).join('');
    return hex_str;
}

